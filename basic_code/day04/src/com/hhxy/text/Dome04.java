package com.hhxy.text;

public class Dome04 {
    public static void main(String[] args) {
        /**定义数组
        * 格式: 数组类型 [] 数组名 = new 数据类型[]{元素1,元素2,....}
         * 简写 : 数组类型 [] 数组名 = {元素1,元素2,...}*/
        int [] array={1,2,3,4,5};
        //遍历(可以输入快捷键fori)
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }
    }
}
